# Unordered: Elements in a set do not have a specific order.
# Unique: Sets automatically remove any duplicate values.
# Mutable: You can add or remove elements from a set, but the elements themselves must be immutable (e.g., numbers, strings, tuples).
# No indexing or slicing: Since sets are unordered, you cannot access elements by index or slice them.


set={"hello", 1, 2, 3, "hello", "test", 3}
print(set)